package beans;

import java.io.Serializable;

public class Calculation  implements Serializable {
    private Integer x, y;
    public Calculation(){}

    public Integer getX() {
        return x;
    }

    public void setX(Integer x) {
        this.x = x;
    }

    public Integer getY() {
        return y;
    }

    public void setY(Integer y) {
        this.y = y;
    }
    
    public Integer getSum() {
        return x + y;
    }
}
